<?php

declare(strict_types=1);

function supported_languages(): array
{
    return ['en', 'pt', 'es'];
}

function normalize_language(?string $language): string
{
    $language = strtolower(trim((string) $language));
    $language = str_replace('_', '-', $language);
    $primary = explode('-', $language, 2)[0];
    return in_array($primary, supported_languages(), true) ? $primary : 'en';
}

function language_locale(string $language): string
{
    return match (normalize_language($language)) {
        'pt' => 'pt-BR',
        'es' => 'es-ES',
        default => 'en-US',
    };
}

function browser_language(): string
{
    $header = strtolower((string) ($_SERVER['HTTP_ACCEPT_LANGUAGE'] ?? ''));
    $candidates = [];

    foreach (explode(',', $header) as $position => $part) {
        $segments = array_map('trim', explode(';', $part));
        $code = normalize_language($segments[0] ?? '');
        $quality = 1.0;
        if (isset($segments[1]) && preg_match('/q=([0-9.]+)/', $segments[1], $matches)) {
            $quality = (float) $matches[1];
        }
        if (preg_match('/^(en|pt|es)(?:-|$)/', $segments[0] ?? '')) {
            $candidates[] = ['code' => $code, 'quality' => $quality, 'position' => $position];
        }
    }

    usort($candidates, static function (array $a, array $b): int {
        return $b['quality'] <=> $a['quality'] ?: $a['position'] <=> $b['position'];
    });

    return $candidates[0]['code'] ?? 'en';
}

function set_language_preference(string $language): string
{
    $language = normalize_language($language);
    $_SESSION['language'] = $language;
    setcookie('lumi_language', $language, [
        'expires' => time() + 31536000,
        'path' => '/',
        'secure' => (!empty($_SERVER['HTTPS']) && $_SERVER['HTTPS'] !== 'off'),
        'httponly' => false,
        'samesite' => 'Lax',
    ]);
    return $language;
}

function interface_language(): string
{
    $queryLanguage = (string) ($_GET['lang'] ?? '');
    if (in_array(normalize_language($queryLanguage), supported_languages(), true) && $queryLanguage !== '') {
        return set_language_preference($queryLanguage);
    }

    $sessionLanguage = (string) ($_SESSION['language'] ?? '');
    if ($sessionLanguage !== '') {
        return normalize_language($sessionLanguage);
    }

    $cookieLanguage = (string) ($_COOKIE['lumi_language'] ?? '');
    if ($cookieLanguage !== '') {
        return normalize_language($cookieLanguage);
    }

    return browser_language();
}

function request_language(?array $user = null): string
{
    if ($user && isset($user['language'])) {
        return normalize_language((string) $user['language']);
    }

    $headerLanguage = (string) ($_SERVER['HTTP_X_LUMI_LANGUAGE'] ?? '');
    if ($headerLanguage !== '') {
        return normalize_language($headerLanguage);
    }

    return interface_language();
}

