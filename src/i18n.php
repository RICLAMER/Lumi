function lumi_t(string $language, string $key, array $replacements = []): string
{
    $language = normalize_language($language);
    $translations = lumi_translations();
    $text = (string) ($translations[$language][$key] ?? $translations['en'][$key] ?? $key);

    foreach ($replacements as $name => $value) {
        $text = str_replace('{' . $name . '}', (string) $value, $text);
    }

    return $text;
}

function language_name(string $language): string
{
    return match (normalize_language($language)) {
        'pt' => 'Português',
        'es' => 'Español',
        default => 'English',
    };
}
